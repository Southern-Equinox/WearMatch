def save_palette(
    palette,
    output_folder: Path
):

    width = 200 * len(palette)

    image = Image.new(
        "RGB",
        (width, 200)
    )

    for i, colour in enumerate(palette):

        block = Image.new(
            "RGB",
            (200, 200),
            colour
        )

        image.paste(
            block,
            (i * 200, 0)
        )

    image.save(
        output_folder / "palette.png"
    )

