import matplotlib.pyplot as plt


def choose_k_from_plot(ks, distortions):
    """
    Displays the elbow plot and lets the user choose K
    by clicking near one of the points.

    Returns
    -------
    int
        Selected K
    """

    fig, ax = plt.subplots(figsize=(7, 5))

    ax.plot(
        ks,
        distortions,
        marker="o",
        linewidth=2,
        markersize=8
    )

    ax.set_title(
        "Choose K by clicking a point"
    )

    ax.set_xlabel("Number of Clusters (K)")
    ax.set_ylabel("Distortion")

    ax.set_xticks(ks)

    ax.grid(True)

    chosen_k = {"value": None}

    def onclick(event):

        if event.xdata is None:
            return

        x = event.xdata

        nearest = min(
            ks,
            key=lambda k: abs(k - x)
        )

        chosen_k["value"] = nearest

        ax.scatter(
            nearest,
            distortions[nearest - 1],
            s=250,
            color="red",
            zorder=5
        )

        ax.set_title(
            f"Selected K = {nearest}"
        )

        fig.canvas.draw()

        plt.pause(0.6)

        plt.close(fig)

    fig.canvas.mpl_connect(
        "button_press_event",
        onclick
    )

    for x, y in zip(ks, distortions):

        ax.annotate(
            str(x),
            (x, y),
            xytext=(0, 10),
            textcoords="offset points",
            ha="center"
        )

    plt.show()

    return chosen_k["value"]



def save_elbow_plot(
    ks,
    distortions,
    output_folder: Path
):

    plt.figure(figsize=(6, 4))

    plt.plot(
        ks,
        distortions,
        marker="o",
        linewidth=2
    )

    plt.xticks(ks)

    plt.title("Elbow Method")

    plt.xlabel("Number of Clusters")

    plt.ylabel("Distortion")

    plt.grid(True)

    plt.tight_layout()

    plt.savefig(
        output_folder / "elbow_plot.png",
        dpi=FIGURE_DPI
    )

    plt.close()


