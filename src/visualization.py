def show_image_comparison(original, resized):
    """
    Display original and resized images side by side.
    """

    fig, ax = plt.subplots(1, 2, figsize=(12, 6))

    ax[0].imshow(original)
    ax[0].set_title(
        f"Original\n{original.size[0]} × {original.size[1]}"
    )
    ax[0].axis("off")

    ax[1].imshow(resized)
    ax[1].set_title(
        f"Downscaled\n{resized.size[0]} × {resized.size[1]}"
    )
    ax[1].axis("off")

    plt.tight_layout()
    plt.show()


